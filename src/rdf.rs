const SHACL_SHAPES: &str = r#"@prefix sh:    <http://www.w3.org/ns/shacl#> .
@prefix spdx:  <http://spdx.org/rdf/terms#> .
@prefix prov:  <http://www.w3.org/ns/prov#> .
@prefix skos:  <http://www.w3.org/2004/02/skos/core#> .
@prefix xsd:   <http://www.w3.org/2001/XMLSchema#> .

<#FileShape> a sh:NodeShape ;
    sh:targetClass spdx:File ;
    sh:property [
        sh:path prov:wasGeneratedBy ;
        sh:minCount 1 ;
        sh:class prov:Activity ;
        sh:name "Lineage link required" ;
    ] ;
    sh:property [
        sh:path spdx:checksum ;
        sh:minCount 1 ;
        sh:class spdx:Checksum ;
        sh:name "Checksum required" ;
    ] .

<#ChecksumShape> a sh:NodeShape ;
    sh:targetClass spdx:Checksum ;
    sh:property [
        sh:path spdx:checksumValue ;
        sh:minCount 1 ;
        sh:datatype xsd:string ;
        sh:name "Hash value required" ;
    ] .

<#CapabilityShape> a sh:NodeShape ;
    sh:targetClass skos:Concept ;
    sh:property [
        sh:path skos:prefLabel ;
        sh:minCount 1 ;
        sh:datatype xsd:string ;
        sh:name "Capability label required" ;
    ] ;
    sh:property [
        sh:path skos:notation ;
        sh:minCount 1 ;
        sh:name "Classification required" ;
    ] .
"#;
